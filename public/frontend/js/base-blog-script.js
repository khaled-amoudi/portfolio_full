$(document).ready(function () {
  $(window).scroll(function () {
    /////////////////////////////////////////////////////////////////////////
    // Start ///////////////////////////////////// scale blog image on scroll
    /////////////////////////////////////////////////////////////////////////
    var scroll = $(window).scrollTop();
    if (scroll < 400) {
      $(".blog-image img").css(
        "transform",
        "scale(" + (1 + scroll / 100 / 28) + ")"
      );
    } else {
      $(".blog-image img").css("transform", "scale(1)");
    }
    /////////////////////////////////////////////////////////////////////////
    // End /////////////////////////////////////// scale blog image on scroll
    /////////////////////////////////////////////////////////////////////////
    //
    //
    //
    //
    //
    //
    //
    /////////////////////////////////////////////////////////////////////////
    // Start ///////////////////////////// change navbar background on scroll
    /////////////////////////////////////////////////////////////////////////
    if (window.scrollY >= 14.0) {
      $("nav.navbar .container-fluid")
        .removeClass("bg-black-100")
        .addClass("bg-black");
    } else {
      $("nav.navbar .container-fluid")
        .removeClass("bg-black")
        .addClass("bg-black-100");
    }
    /////////////////////////////////////////////////////////////////////////
    // End /////////////////////////////// change navbar background on scroll
    /////////////////////////////////////////////////////////////////////////
  });

  /////////////////////////////////////////////////////////////////////////
  // Start ///////////////////////////// Activate Mobile Hamburger & Navbar
  /////////////////////////////////////////////////////////////////////////
  $("#blogMobileHamburger").on("click", function () {
    $(this).toggleClass("active");
    $("#blogMobileNavbar").toggleClass("active");
    if ($("#blogMobileNavbar").hasClass("active")) {
      $("#blogMobileNavbar").slideDown();
    } else {
      $("#blogMobileNavbar").slideUp();
    }
  });
  /////////////////////////////////////////////////////////////////////////
  // End /////////////////////////////// Activate Mobile Hamburger & Navbar
  /////////////////////////////////////////////////////////////////////////
});